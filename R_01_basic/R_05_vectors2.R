# vector calculus. behaves just like an array in numpy
earning <- c(50, 100, 30)
earning * 3
earning - 10
earning + 5
earning / 100
earning ^ 2

# vector operates on vector
expense <- c(30, 40, 80)
earning + expense
earning - expense
earning * expense
earning / expense

# logic operators also works likewise
earning > expense
earning < expense
earning == expense
earning >= expense
earning <= expense

# sum()
sum(earning)
length(earning)
summary(earning)
